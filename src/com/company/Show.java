package com.company;

public class Show {
    private String name;
    private String description;

    //constructor
    public Show(){
        name = "";
        description = "";

    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }


    public String getDescription() {
        return description;
    }

    public void setDescription(String description) {
        this.description = description;
    }


}
